#include <iostream>

int main() {
	std::cout << "HelloWorld!" << std::endl;

	// STEP 1：文字列の内容を変えよう
	// この文字列以外のものは動かさないでね
	std::cout << "この文字列と適当に好きなものに変えよう" << std::endl;


	// STEP 2：このコミットのメッセージを変えよう
	std::cout << "このコミットのメッセージを変えよう" << std::endl;

	// Step 3：コミットの順番を入れ替えよう
	std::cout << "コミットの順番を入れ替えよう" << std::endl;

	return 0;
}